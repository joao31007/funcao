def calculadora(num1,num2,operacao):
    if operacao == "+":
        print(num1 + num2)
    elif operacao == "-":
        print(num1 - num2)
    elif operacao == "*":
        print(num1 * num2)
    elif operacao == "/":
            print(num1 / num2)
    

calculadora(10, 5, "+")     
calculadora(15, 2, "-")
calculadora(20, 5, "*")
calculadora(40, 10, "/")
